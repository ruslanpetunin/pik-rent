    /**
     * {@inheritdoc}
     */
    public function safeUp()
    {
        foreach ($this->aStreets as $sCity => $aDistricts) {
            $oCity = City::findOne(['c_hash_name' => $this->getHash($sCity)]) ?: new City();
            $oCity->c_name = $sCity;

            $oCity->save();
            $this->rememberAllWordsFromString($sCity);

            foreach ($aDistricts as $sDistrict => $aStreets) {
                $oDistrict = District::findOne(['d_hash_name' => $this->getHash($sDistrict)]) ?: new District();
                $oDistrict->d_name = $sDistrict;
                $oDistrict->d_city_id = $oCity->c_id;

                $oDistrict->save();
                $this->rememberAllWordsFromString($sDistrict);

                for($i = 0; $i < count($aStreets); $i++) {
                    $oStreet = Street::findOne(['s_hash_name' => $this->getHash($aStreets[$i])]) ?: new Street();
                    $oStreet->s_name = $aStreets[$i];
                    $oStreet->s_district_id = $oDistrict->d_id;

                    $oStreet->save();
                    $this->rememberAllWordsFromString($aStreets);
                }
            }
        }
    }

    /**
     * {@inheritdoc}
     */
    public function safeDown()
    {
        $this->delete('streets', 's_id > 0');
        $this->delete('districts', 'd_id > 0');
        $this->delete('cities', 'c_id > 0');
    }

    public function rememberAllWordsFromString($sText)
    {
        return preg_replace_callback(
            '/[а-яА-ЯA-Za-z]*/iu',
            function($sWord) {
                if ($sWord[0]) {
                    $sHash = $this->getHash($sWord[0]);

                    if (!Dictionary::findOne(['d_hash' => $sHash])) {
                        Yii::$app->spellChecker->rememberWord($sWord[0]);
                    }
                }

                return $sWord[0];
            },
            $sText
        );
    }
